  chartInstance->c5_is_active_c5_Quadrotor = c5_g_emlrt_marshallIn(chartInstance,
    sf_mex_dup(sf_mex_getcell(c5_u, 3)), "is_active_c5_Quadrotor");
  sf_mex_destroy(&c5_u);
  c5_update_debugger_state_c5_Quadrotor(chartInstance);
